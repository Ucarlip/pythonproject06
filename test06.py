# For

for x in range(5) :
    print('IoT {x}')

print('+++++++++++')

for y in range(10) :
    print('SAU {y} ')

print('+++++++++++')

for z in range(2, 10) :
    print(f'Hi {z}')

print('+++++++++++')

for a in range(3, 10, 2) :
    print(f'Hey {a}')

print('+++++++++++')

for b in 'Welcome to Thailand' :
    print(f'Hoo {b}')